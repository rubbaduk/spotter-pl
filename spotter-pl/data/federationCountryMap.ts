// get country for a federation
export function getCountryForFederation(federation: string): string {

  if (federation === 'all') return 'International';
  if (federation.startsWith('all-')) {
    const country = federation.replace('all-', '').replace('-tested', '');
    // convert to title case
    return country.charAt(0).toUpperCase() + country.slice(1).toLowerCase();
  }
  
  // look up in map
  return federationCountryMap[federation] || 'International';
}
